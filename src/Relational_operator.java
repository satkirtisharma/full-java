public class Relational_operator {
    public static void main(String[] args) {
        // 6 Type of relational operator
        int a=5;
        int b=2;
        System.out.println(a==b);
        b=5;
        System.out.println(a==b);
        System.out.println(a!=b);
        b=3;
        System.out.println(a!=b);
        System.out.println(a>b);
        System.out.println(b>a);
        System.out.println(a<b);
        System.out.println(b<a);
        b=5;
        System.out.println(a>=b);
        System.out.println(a<=b);
    }
}
